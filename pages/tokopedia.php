                        <div id="cara-bayar-pulsa">
                          <p style="text-align: justify;">Tokopedia menyediakan berbagai macam metode pembayaran yang dapat kamu gunakan untuk melakukan pembelian pulsa diantaranya, pembayaran dengan Saldo Tokopedia, Kartu Kredit, BCA Klikpay, KlikBCA, Mandiri Clickpay, Mandiri e-Cash, BRi e-Pay, BRI Virtual Account, Alfagroup, Indomaret, dan Pos Indonesia.</p>
                          <div style="text-align: justify;">
                            <h3 id="bayar-pulsa-dengan-saldo" style="text-align: left;">Bayar Pulsa dengan Saldo Tokopedia</h3>
                          </div>
                          <p style="text-align: justify;">Jika Saldo Tokopedia kamu mencukupi, kamu bisa langsung melakukan pembayaran pulsa dengan klik Bayar Instan dengan Saldo Tokopedia di homepage Tokopedia Pulsa.</p>
                          <p style="text-align: justify;">Namun perlu diingat, jika kamu melakukan pembayaran dengan cara tersebut, kamu tidak bisa menggunakan Kode Voucher. Oleh karena itu, jika memiliki Kode Voucher dan ingin menggunakannya untuk membeli pulsa, pastikan kamu langsung mengklik tombol Beli setelah memilih nominal pulsa. Pada halaman selanjutnya, kamu bisa pilih Saldo Tokopedia, lalu klik Gunakan Voucher Tokopedia.</p>
                          <div style="text-align: justify;">
                            <h3 id="bayar-pulsa-dengan-kartu-kredit" style="text-align: left;">Bayar Pulsa dengan Kartu Kredit</h3>
                          </div>
                          <p style="text-align: justify;">Kamu bisa melakukan pembayaran dengan kartu kredit untuk minimum pembelian Rp. 10.000 dan maksimum total&nbsp;<em>invoice</em>&nbsp;Rp. 1.000.000 per hari. Pembeli akan dikenakan biaya 1.5% dari total transaksi.</p>
                          <p style="text-align: justify;">Cara membayar pulsa dengan kartu kredit pun mudah, cukup mengisi data sesuai kartu kredit kamu, lalu klik Bayar Sekarang.</p>
                          <div style="text-align: justify;">
                            <h3 id="bayar-pulsa-dengan-bca-klikpay" style="text-align: left;">Bayar Pulsa dengan BCA KlikPay</h3>
                          </div>
                          <ol style="text-align: justify;">
                            <li>Akses situs pulsa Tokopedia</li>
                            <li>Pilih nominal pulsa yang dibutuhkan dan masukkan nomor ponsel kamu. Klik Beli.</li>
                            <li>Pilih metode pembayaran BCA Klikpay.</li>
                            <li>Klik Bayar.</li>
                            <li>kamu akan terhubung dengan halaman BCA KlikPay.</li>
                            <li><em>Login</em>&nbsp;ke akun BCA KlikPay.</li>
                            <li>Akan muncul detail transaksi pulsa kamu. Klik Kirim.</li>
                            <li>Klik Kirim OTP. Masukkan kode OTP, lalu klik Bayar.</li>
                          </ol>
                          <div style="text-align: justify;">
                            <h3 id="bayar-pulsa-dengan-klikbca" style="text-align: left;">Bayar Pulsa dengan KlikBCA</h3>
                          </div>
                          <p style="text-align: justify;">Kamu bisa melakukan pembelian pulsa dengan metode pembayaran instan KlikBCA, untuk minimum pembelian Rp10.000. Berikut caranya:</p>
                          <ol style="text-align: justify;">
                            <li>Akses situs pulsa Tokopedia</li>
                            <li>Pilih nominal pulsa yang dibutuhkan dan masukkan nomor ponsel kamu. Klik Beli.</li>
                            <li>Pilih metode pembayaran Klik BCA.</li>
                            <li>Masukkan&nbsp;<em>User ID</em>&nbsp;KlikBCA kamu, lalu klik Bayar.</li>
                            <li>Masuk ke situs www.klikbca.com, kemudian&nbsp;<em>login</em>&nbsp;dengan akun KlikBCA kamu.</li>
                            <li>Pilih menu Pembayaran E-commerce.</li>
                            <li>Pilih kategori Market Place, kemudian Pilih nama perusahaan PT. Tokopedia.</li>
                            <li>Pilih transaksi yang ingin kamu bayar.</li>
                            <li>Masukkan Kode otorisasi keyBCA untuk memproses transaksi.</li>
                          </ol>
                          <div style="text-align: justify;">
                            <h3 id="bayar-pulsa-dengan-mandiri-clickpay" style="text-align: left;">Bayar Pulsa dengan Mandiri Clickpay</h3>
                          </div>
                          <ol style="text-align: justify;">
                            <li>Akses pulsa Tokopedia</li>
                            <li>Pilih nominal pulsa yang dibutuhkan dan masukkan nomor ponsel kamu. Klik Beli.</li>
                            <li>Pilih metode pembayaran Mandiri Clickpay.</li>
                            <li>Masukkan nomor kartu debit dan respon token. Lalu klik Bayar.</li>
                          </ol>
                          <div style="text-align: justify;">
                            <h3 id="bayar-pulsa-dengan-mandiri-ecash" style="text-align: left;">Bayar Pulsa dengan Mandiri e-Cash</h3>
                          </div>
                          <ol style="text-align: justify;">
                            <li>Akses pulsa Tokopedia</li>
                            <li>Pilih nominal pulsa yang dibutuhkan dan masukkan nomor ponsel kamu. Klik Beli.</li>
                            <li>Pilih metode pembayaran Mandiri e-Cash. Lalu klik Bayar.</li>
                            <li>kamu akan diarahkan ke halaman Mandiri e-Cash.</li>
                            <li>Masukkan nomor&nbsp;<em>handphone</em>&nbsp;dan PIN Mandiri e-Cash kamu, klik Lanjut.</li>
                            <li>Mandiri akan mengirimkan Kode OTP melalui SMS.</li>
                            <li>Masukkan Kode OTP. Klik Bayar.</li>
                          </ol>
                          <div style="text-align: justify;">
                            <h3 id="bayar-pulsa-dengan-bri-epay" style="text-align: left;">Bayar Pulsa dengan BRI e-Pay</h3>
                          </div>
                          <ol style="text-align: justify;">
                            <li>Akses pulsa Tokopedia</li>
                            <li>Pilih nominal pulsa yang dibutuhkan dan masukkan nomor ponsel kamu. Klik Beli.</li>
                            <li>Pilih metode pembayaran e-Pay BRI. Klik Bayar.</li>
                            <li>kamu akan diarahkan ke halaman BRI ePayment.</li>
                            <li>Pilih tab Internet Banking, lalu masukkan&nbsp;<em>User ID</em>&nbsp;dan password. Klik Submit.</li>
                          </ol>
                          <div style="text-align: justify;">
                            <h3 id="bayar-pulsa-dengan-briva" style="text-align: left;">Bayar Pulsa dengan BRI&nbsp;<em>Virtual Account</em>&nbsp;(BRIVA)</h3>
                          </div>
                          <ol style="text-align: justify;">
                            <li>Pilih nominal pulsa yang dibutuhkan dan masukkan nomor ponsel kamu. Klik Beli.</li>
                            <li>Akan muncul detail pembelian pulsa, klik Lanjut.</li>
                            <li>Pilih metode pembayaran Virtual Account, lalu pilih BRIVA (BRI Virtual Account). Klik Bayar Sekarang.</li>
                            <li>Di halaman berikutnya akan muncul Kode Pembayaran.</li>
                            <li>Lakukan pembayaran dengan menggunakan kode tersebut. Untuk pilihan cara pembayaran, bisa kamu bisa lihat&nbsp;<a href="https://www.tokopedia.com/bantuan/cara-bayar-briva">sini &gt;</a></li>
                            <li>Masukkan&nbsp;<em>password</em>&nbsp;dan&nbsp;<em>paycode</em>, lalu klik Submit. Paycode adalah kode pengamanan yang akan dikirim ke nomor handphone kamu yang sudah terdaftar sebagai mTOKEN.</li>
                          </ol>
                          <p style="text-align: justify;">Catatan: Untuk pembayaran dengan T-Bank, pilih T-Bank di halaman e-Pay BRI dan ikuti langkah selanjutnya.</p>
